def aud(usd):
    """
    Convert U.S Dollars to Australian Dollars
    """
    return usd * 1.25

def cad(usd):
    """
    Convert U.S Dollars to Canadian Dollars
    """
    return usd * 1.23

def jpy(usd):
    """
    Convert U.S Dollars to Japanese Yen
    """
    return usd * 109.89

def eur(usd):
    """
    Convert U.S Dollars to Euros
    """
    return usd * 0.80

def chf(usd):
    """
    Convert U.S Dollars to Swiss Francs
    """
    return usd * 0.93
    